    st.subheader("📸 予想画像生成")
    if "final_res" in st.session_state:
        df = st.session_state["final_res"]
        
        if st.button("✨ モダンな予想画像を生成", use_container_width=True, type="primary"):
            with st.spinner("デザイン構築中..."):
                # 画像生成
                final_img = create_final_image(r_place, r_num, df)
                
                # プレビュー表示
                st.image(final_img, caption="SNS投稿用プレビュー", use_container_width=True)
                
                # ダウンロードボタン
