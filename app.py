def create_modern_sns_image(race_info, df_sorted):
    # Twitter/X推奨比率 (16:9)
    width, height = 1280, 720
    img = Image.new('RGB', (width, height), color=(255, 255, 255))
    draw = ImageDraw.Draw(img)
    
    # フォント読み込み
    font_path = "NotoSansJP-Regular.ttf" 
    try:
        f_title = ImageFont.truetype("NotoSansJP-Bold.ttf", 60) # タイトルは太字
        f_header = ImageFont.truetype(font_path, 36)
        f_pct = ImageFont.truetype("NotoSansJP-Bold.ttf", 80)
        f_boat = ImageFont.truetype("NotoSansJP-Bold.ttf", 120)
        f_label = ImageFont.truetype(font_path, 24)
    except:
        f_title = f_header = f_pct = f_boat = f_label = ImageFont.load_default()

    # --- 背景デザイン ---
    # 上部ヘッダー（濃紺 gradient風）
    for y in range(120):
        color = (20 + y//4, 40 + y//3, 80 + y//2)
        draw.line([(0, y), (width, y)], fill=color)
        
    # タイトル
    title_text = f"🎯 {race_info['place']} {race_info['num']}R 事前予想"
    draw.text((40, 30), title_text, font=f_title, fill=(255, 255, 255))
    draw.text((width - 320, 50), race_info['date'], font=f_header, fill=(200, 200, 200))

    # --- 上位3艇のカードデザイン ---
    card_width = 380
    card_height = 480
    start_x = 60
    y_place = 160
    
    # 艇ごとのカラー定義（1-6号艇）
    boat_colors = {
        1: (230, 126, 34), # 橙
        2: (52, 152, 219),  # 青
        3: (231, 76, 60),   # 赤
        4: (241, 196, 15),  # 黄
        5: (46, 204, 113),  # 緑
        6: (149, 165, 166)  # 灰
    }

    for i in range(3):
        row = df_sorted.iloc[i]
        boat_no = int(row['艇番'])
        pct = float(row['予想％'])
        curr_x = start_x + (card_width + 30) * i
        
        # カード背景と枠線
        draw.rounded_rectangle([curr_x, y_place, curr_x + card_width, y_place + card_height], radius=20, fill=(250, 250, 250), outline=(200,200,200), width=2)
        
        # 順位ラベル
        rank_text = ["🥇 1番手", "🥈 2番手", "🥉 3番手"][i]
        draw.text((curr_x + 20, y_place + 20), rank_text, font=f_header, fill=(50, 50, 50))
        
        # 艇番（大きく）
        boat_txt = f"{boat_no}"
        draw.text((curr_x + card_width//2 - 40, y_place + 100), boat_txt, font=f_boat, fill=boat_colors.get(boat_no, (0,0,0)))
        draw.text((curr_x + card_width//2 + 50, y_place + 180), "号艇", font=f_header, fill=(100, 100, 100))

        # 予想％（強調）
        pct_txt = f"{pct:.1f}%"
        tw = draw.textlength(pct_txt, font=f_pct)
        draw.text((curr_x + card_width//2 - tw//2, y_place + 300), pct_txt, font=f_pct, fill=(211, 47, 47))

    # フッター
    draw.text((width - 450, height - 40), "Generated by Boat-AI Pro Analytica", font=f_label, fill=(180,180,180))

    return img

